fun main() {

    // Boolean and() dan or()
    // selain menggunakan cara sebelumnya didalam kotlin bisa juga
    // menggunakan and() dam or()

    //Deklaraso Variable
    val x: Boolean = true
    val y: Boolean = false
    val z: Boolean = true

    // Penggunaam and()
    println("Nilai x.and(y) adalah " + x.and(y))
    // Penggunaam or()
    println("Nilai x.or(y) adalah " + x.or(y))
    // Penggunaam and()
    println("Nilai x.and(z) adalah " + x.and(z))

}