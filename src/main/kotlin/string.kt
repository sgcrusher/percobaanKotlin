fun main() {

    // val merupakan variable, nama merupakan identifier variable,
    // String merupakan tipe Data, dan "Sandy Gustyanto" merupakan inisiasi variable
    val nama : String = "Sandy Gustyanto"
    println(nama)
    println("===================================\n")

    // variable mempunyai 2 tipe yaitu val dan var
    // val merupakan variable yang nilainya tidak bisa dirubah
    // var merupakan variable bisa dirubah
    var hewan : String = "Ini Kucing"
    hewan = "Ini bukan kucing"
    println(hewan)
    // diatas merupakan penggunaan dari kedua tipe variable yaitu val dan var
    // inisiasi variable hewan dapat diubah jika menggunakan tipe var
    // jika menggunakan tipe val maka tidak bisa diubah
}