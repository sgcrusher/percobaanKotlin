fun main() {

    // Ekpresi Boolean
    // Tipe data ini biasa digunakan untuk if else ekspresion
    // Tipe data ini biasa digunakan dalam operator matematika
    // operator >, <, >=, <=, ==, !=

    println("Penggunaan ekpresi boolean pada operator matematika")
    println("============================================================")
    // Deklarasi Variable
    val x: Int = 20
    val y: Int = 25

    // Operator lebih dari >
    println("Operator Lebih dari > ")
    println("Apakah Benar Nilai x > y = " + (x>y))
    println("------------------------------------------------")
    // Operator Kurang dari <
    println("Operator Kurang dari < ")
    println("Apakah Benar Nilai x < y = " + ( x < y ))
    println("------------------------------------------------")
    // Operator lebih dari sama dengan >=
    println("Operator Lebih dari sama dengan >= ")
    println("Apakah Benar Nilai x >= y = " + ( x >= y ))
    println("------------------------------------------------")
    // Operator Kurang dari sama dengan <=
    println("Operator Kurang dari sama dengan <= ")
    println("Apakah Benar Nilai x <= y = " + ( x <= y ))
    println("------------------------------------------------")
    // Operator sama dengan ==
    println("Operator sama dengan == ")
    println("Apakah Benar Nilai x == y = " + ( x == y ))
    println("------------------------------------------------")
    // Operator Tidak sama dengan !=
    println("Operator Tidak sama dengan != ")
    println("Apakah Benar Nilai x != y = " + ( x != y ))
    println("------------------------------------------------")

}